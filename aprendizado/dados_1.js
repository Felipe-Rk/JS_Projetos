
// para criar uma variavel ----- var exemplo

var nome = "Caneta"
var quantidade = 10 
var preco = 6.4
var imposto = 1.5
var precoFinal = preco + imposto

console.log("produto:",nome)
console.log("Quantidade de", nome , "disponiveis: ",quantidade)
console.log("Valor:", preco)
console.log("Imposto cobrado:", imposto)
console.log("Preço final contando o imposto", precoFinal)

// Variavel pode ser modificada durante o code exemplo

nome = "Caneta Bic"

console.log(nome)


// Nova forma mais adequada de criar uma variavel é usando let - exemplo : 

let nome1 = "Caneta"
let quantidade1 = 10 
let preco1 = 6.4
let imposto1 = 1.5
let precoFinal1 = preco1 + imposto1

console.log("produto:",nome1)
console.log("Quantidade de", nome1 , "disponiveis: ",quantidade1)
console.log("Valor:", preco1)
console.log("Imposto cobrado:", imposto1)
console.log("Preço final contando o imposto", precoFinal1)